package chap04;

// 1과 100 사이의 값을 반복적으로 입력해서 컴퓨터가 생각한 값을 맞히면 끝나는 숫자 맞히기 게임
// 컴퓨터는 자신의 값과 비교해서 결과를 알려주고, 숫자를 맞히면 게임이 끝나고 몇 번 만에 숫자를 맞혔는지 알려준다.

public class Exercise4_10 {
	public static void main(String[] args) {
		// 1~100사이의 임의의 값을 얻어서 answer에 저장
		int answer = (int) (Math.random() * 100) + 1;
		int input = 0; // 사용자 입력을 저장할 공간
		int count = 0; // 시도 횟수를 세기 위한 변수

		// 화면으로부터 사용자 입력을 받기 위해 scanner 클래스 사용
		java.util.Scanner s = new java.util.Scanner(System.in);
		// System.out.println(answer);
		do {
			count++;
			System.out.print("1과 100 사이의 값을 입력하세요 :");
			input = s.nextInt(); // 입력 받은 값을 변수 input에 저장
			if (input > answer) {
				System.out.println("더 작은 수로 다시 시도해보세요.");
			} else if (input < answer) {
				System.out.println("더 큰 수로 다시 시도해보세요.");
			} else {
				System.out.printf("정답입니다. %d번 만에 정답을 맞히셨습니다.", count);
				break;
			}
		} while (true);
	}
}